    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String n1 = txtNumeroUm.getText();
        String n2 = txtNumeroDois.getText();
        String n3 = txtNumeroTres.getText();
        String n4 = txtNumeroQuatro.getText();
        float a = Float.parseFloat(n1);
        float b = Float.parseFloat(n2);
        float c = Float.parseFloat(n3);
        float d = Float.parseFloat(n4);
        calc.setA(a);
        calc.setB(b);
        calc.setC(c);
        calc.setD(d);
        float r = calc.media();
        txtResultado.setText(String.valueOf(r));
    }//GEN-LAST:event_jButton1MouseClicked

